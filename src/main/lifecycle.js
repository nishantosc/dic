function bootApp(name) {
  return {
    name,
    ready: true,
    startedAt: Date.now(),
    sessionId: `session_${Math.random().toString(16).slice(2)}`
  }
}

function stopApp(state) {
  return { ...state, ready: false, stoppedAt: Date.now() }
}

