function canInvokeDiscordChannel(channel, map) {
  if (!Object.prototype.hasOwnProperty.call(map, channel)) return false
  return map[channel] === 'allow'
}

function filterDiscordPayload(payload) {
  const clone = { ...payload }
  delete clone.token
  delete clone.cookie
  return clone
}

